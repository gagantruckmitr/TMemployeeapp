    <script>
    $(document).ready(function() {
      let selectedBoxId = "";
      let selectedTrucks = {}; // Store selected truck slugs

      $(".o-bTDyCI").click(function () {
          selectedBoxId = $(this).attr("id"); // Store the ID of the clicked box
      });
        $("#brandSelect").change(function() {
            var brand_id = $(this).val();

            $.ajax({
                url: "/get-trucks/" + brand_id,
                type: "GET",
                success: function(data) {
                    $("#variantSelect").html(data.options);

                    // ðŸ”¹ Automatically switch to the "Variant" tab
                    $('#variant-tab').tab('show');
                }
            });
        });

        $("#variantSelect").change(function() {
            var slug = $(this).val();

            $.ajax({
                url: "/get-truck-details/" + slug,
                type: "GET",
                success: function(data) {
                    $("#"+selectedBoxId).html(data.html); // Replace Truck Box
                    selectedTrucks[selectedBoxId] = slug; // Store slug
                    resetAndCloseModal();
                }
            });
        });

        $("#compareButton").click(function () {
            let truckSlugs = Object.values(selectedTrucks); // Get selected slugs

            if (truckSlugs.length < 2) {
                alert("Please select at least 2 trucks to compare.");
                return;
            }
            
            // Redirect to match the Laravel route structure
            window.location.href = "/compare/" + truckSlugs.join("/");
        });

        function resetAndCloseModal() {
            $("#brandSelect").val(""); // Reset brand dropdown
            $("#variantSelect").val(""); // Reset variant dropdown
            $("#compareTruckModal").modal("hide"); // Close modal
        }
    });
    </script>

